"""Tests for PlasimClimateDataset.

Per the smoke-test contract in ``hpc/delta.md``, datapipe smoke tests must
hit real disk. We use the committed-out-of-repo PLASIM fixture at
``$AI_ROSSBY_TEST_DATA/plasim/smoke_month.zarr`` (generated by
``tools/data/plasim/pangu_h5_to_zarr.py`` from PanguWeather v2.0 sim52,
year 100, first month at 6-hourly cadence; 120 timesteps total). Unit tests
that don't need GPU also exercise this fixture — opening a real Zarr is the
only way to verify the channel-routing layer-stacking semantics that are
core to this datapipe's contract.

Both unit and smoke tests are skipped (not failed) when the fixture is
missing — keeps the test suite green on hosts where the fixture hasn't been
staged.
"""

from __future__ import annotations

import os
import warnings
from pathlib import Path

import pytest
import torch

with warnings.catch_warnings():
    warnings.filterwarnings("ignore", category=Warning, module=r"physicsnemo\.experimental.*")
    from physicsnemo.experimental.datapipes.plasim import (
        LeadTimePairSampler,
        PlasimClimateDataset,
    )


def _fixture_path() -> Path | None:
    root = os.environ.get("AI_ROSSBY_TEST_DATA")
    if not root:
        return None
    p = Path(root) / "plasim" / "smoke_month.zarr"
    return p if p.exists() else None


_HAS_FIXTURE = _fixture_path() is not None
_skip_no_fixture = pytest.mark.skipif(
    not _HAS_FIXTURE,
    reason="$AI_ROSSBY_TEST_DATA/plasim/smoke_month.zarr is not staged; generate "
    "via `tools/data/plasim/pangu_h5_to_zarr.py` then re-run.",
)


