@_skip_no_fixture
def test_dataset_sample_shapes():
    ds = PlasimClimateDataset(_fixture_path())
    sample = ds[(0, 1)]

    H, W = ds.horizontal_resolution
    L = ds.num_levels
    assert sample["surface_in"].shape == (2, H, W)
    assert sample["constant_boundary"].shape == (3, H, W)
    assert sample["varying_boundary"].shape == (3, H, W)
    assert sample["upper_air_in"].shape == (5, L, H, W)
    assert sample["target_surface"].shape == (2, H, W)
    assert sample["target_upper_air"].shape == (5, L, H, W)
    assert sample["diagnostic"].shape == (1, H, W)
    assert int(sample["lead_time"]) == 1
    assert int(sample["time_idx"]) == 0

    # Prognostic fields must be finite (these flow through gradient computation).
    # Constant boundaries (e.g. `lsm` land-sea-mask) and varying boundaries may
    # carry NaN at high-latitude or polar grid points by design — leave those
    # for the normalization/mask transforms to handle, don't assert here.
    for k in ("surface_in", "upper_air_in", "target_surface", "target_upper_air"):
        assert torch.isfinite(sample[k]).all(), f"non-finite values in {k}"


