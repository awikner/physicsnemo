def test_sampler_yields_expected_count_and_range():
    s = LeadTimePairSampler(
        dataset_length=100,
        forecast_lead_times=[1, 5, 10],
        num_samples=64,
        seed=0,
    )
    pairs = list(s)
    assert len(pairs) == len(s) == 64
    for start, lead in pairs:
        assert 0 <= start < 100 - 10  # capped at strictest valid bound
        assert lead in (1, 5, 10)


def test_sampler_shuffle_seed_determinism():
    a = list(LeadTimePairSampler(100, [1, 5], num_samples=20, seed=42))
    b = list(LeadTimePairSampler(100, [1, 5], num_samples=20, seed=42))
    assert a == b
    c = list(LeadTimePairSampler(100, [1, 5], num_samples=20, seed=43))
    assert a != c


def test_sampler_ddp_slicing_partitions_positions():
    """Each rank owns a disjoint set of *positions* in the per-epoch sample
    sequence. Value-collisions are fine (two ranks at different positions can
    randomly draw the same (start, lead) pair); position-collisions are not.
    """
    common = dict(dataset_length=200, forecast_lead_times=[1, 5], num_samples=64, seed=0)
    full = list(LeadTimePairSampler(rank=0, world_size=1, **common))
    rank0 = list(LeadTimePairSampler(rank=0, world_size=2, **common))
    rank1 = list(LeadTimePairSampler(rank=1, world_size=2, **common))
    assert len(rank0) == len(rank1) == 32
    # rank0 owns positions [0, 2, 4, ...]; rank1 owns [1, 3, 5, ...]; together
    # they reproduce the full single-rank sequence in interleaved order.
    interleaved = [None] * (len(rank0) + len(rank1))
    interleaved[0::2] = rank0
    interleaved[1::2] = rank1
    assert interleaved == full[: len(interleaved)]


def test_sampler_set_epoch_changes_order():
    s = LeadTimePairSampler(100, [1, 5], num_samples=10, seed=0)
    s.set_epoch(0)
    a = list(s)
    s.set_epoch(1)
    b = list(s)
    assert a != b


def test_sampler_rejects_lead_longer_than_dataset():
    with pytest.raises(ValueError, match="no valid pairs exist"):
        LeadTimePairSampler(dataset_length=10, forecast_lead_times=[100])


