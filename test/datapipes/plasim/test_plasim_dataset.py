@_skip_no_fixture
def test_dataset_layout_reads_attrs():
    ds = PlasimClimateDataset(_fixture_path())

    # Channel groups match the SFNO_PLASIM_H5_DERECHO_5412.yaml config the
    # fixture was generated from (PanguWeather sim52).
    assert ds.layout.surface_variables == ["pl", "tas"]
    assert ds.layout.constant_boundary_variables == ["lsm", "sg", "z0"]
    assert ds.layout.varying_boundary_variables == ["sst", "rsdt", "sic"]
    assert ds.layout.diagnostic_variables == ["pr_6h"]
    assert ds.layout.pressure_upper_air_variables == ["zg"]
    assert ds.layout.sigma_upper_air_variables == ["ta", "ua", "va", "hus"]
    assert ds.layout.calendar == "proleptic_gregorian"
    assert ds.layout.data_timedelta_hours == 6

    # Level systems both present, equal length (required for v1 concat).
    assert len(ds.sigma_levels) == 10
    assert len(ds.pressure_levels) == 10
    assert ds.num_levels == 10
    assert ds.horizontal_resolution == (64, 128)

    # Concat order: sigma vars first, pressure vars second.
    assert ds.upper_air_variable_names == ["ta", "ua", "va", "hus", "zg"]


@_skip_no_fixture
def test_dataset_sample_shapes():
    ds = PlasimClimateDataset(_fixture_path())
    sample = ds[(0, 1)]

    H, W = ds.horizontal_resolution
    L = ds.num_levels
    assert sample["surface_in"].shape == (2, H, W)
    assert sample["constant_boundary"].shape == (3, H, W)
    assert sample["varying_boundary"].shape == (3, H, W)
    assert sample["upper_air_in"].shape == (5, L, H, W)
    assert sample["target_surface"].shape == (2, H, W)
    assert sample["target_upper_air"].shape == (5, L, H, W)
    assert sample["diagnostic"].shape == (1, H, W)
    assert int(sample["lead_time"]) == 1
    assert int(sample["time_idx"]) == 0

    # Prognostic fields must be finite (these flow through gradient computation).
    # Constant boundaries (e.g. `lsm` land-sea-mask) and varying boundaries may
    # carry NaN at high-latitude or polar grid points by design — leave those
    # for the normalization/mask transforms to handle, don't assert here.
    for k in ("surface_in", "upper_air_in", "target_surface", "target_upper_air"):
        assert torch.isfinite(sample[k]).all(), f"non-finite values in {k}"


@_skip_no_fixture
def test_dataset_int_index_defaults_to_lead_1():
    ds = PlasimClimateDataset(_fixture_path())
    sample_pair = ds[(3, 1)]
    sample_int = ds[3]
    assert torch.equal(sample_pair["surface_in"], sample_int["surface_in"])
    assert int(sample_int["lead_time"]) == 1


