@pytest.mark.smoke
@pytest.mark.cuda
@_skip_no_fixture
def test_plasim_dataset_smoke_iterates_on_cuda():
    """Real PLASIM Zarr → dataset → iterate N batches on CUDA.

    Per the smoke-test contract in ``hpc/delta.md``: datapipe smoke tests
    verify shape/dtype/device + channel-routing on real disk-backed data.
    Iteration + moving to CUDA exercises the full Zarr → torch path; the
    contract does **not** require a successful model forward here. Once the
    NetCDF-stats normalization transform lands (next commit), this is
    extended to a model forward+backward+step.
    """
    if not torch.cuda.is_available():
        pytest.skip("smoke test requires CUDA")

    device = torch.device("cuda:0")
    ds = PlasimClimateDataset(_fixture_path())
    H, W = ds.horizontal_resolution
    L = ds.num_levels

    sampler = LeadTimePairSampler(
        dataset_length=len(ds),
        forecast_lead_times=[1, 4],
        num_samples=4,
        seed=0,
    )

    n_iter = 0
    for start, lead in sampler:
        sample = ds[(start, lead)]
        moved = {k: v.to(device) for k, v in sample.items()}
        # Channel-routing + dtype + device contract.
        assert moved["surface_in"].shape == (2, H, W)
        assert moved["constant_boundary"].shape == (3, H, W)
        assert moved["varying_boundary"].shape == (3, H, W)
        assert moved["upper_air_in"].shape == (5, L, H, W)
        assert moved["target_surface"].shape == (2, H, W)
        assert moved["target_upper_air"].shape == (5, L, H, W)
        assert moved["diagnostic"].shape == (1, H, W)
        for k in ("surface_in", "upper_air_in", "target_surface", "target_upper_air"):
            assert moved[k].device.type == "cuda"
            assert moved[k].dtype == torch.float32
            assert torch.isfinite(moved[k]).all(), f"non-finite {k} at start={start}"
        n_iter += 1
    assert n_iter == 4

    torch.cuda.empty_cache()


@pytest.mark.smoke
@pytest.mark.cuda
@_skip_no_fixture
@_skip_no_stats
def test_plasim_dataset_with_normalizer_drives_pangu_plasim():
    """End-to-end: real Zarr → normalizer → PanguPlasim forward + backward + step on CUDA.

    Closes the loop on Phase 2: PLASIM stats are loaded, applied to the sample
    on-device, the model's transformer sees normalized inputs and produces a
    finite loss, AdamW takes one step. Anything non-finite here means either
    the stats files are misaligned with the Zarr layout, or the model wiring
    has regressed on real-data magnitudes.
    """
    if not torch.cuda.is_available():
        pytest.skip("smoke test requires CUDA")

    device = torch.device("cuda:0")
    ds = PlasimClimateDataset(_fixture_path())
    normalizer = PlasimNormalizer.from_dataset(
        ds, mean_path=_MEAN_PATH, std_path=_STD_PATH
    ).to(device)

    model = PanguPlasim(
        surface_variables=ds.layout.surface_variables,
        upper_air_variables=ds.upper_air_variable_names,
        constant_boundary_variables=ds.layout.constant_boundary_variables,
        varying_boundary_variables=ds.layout.varying_boundary_variables,
        diagnostic_variables=ds.layout.diagnostic_variables,
        levels=ds.sigma_levels,  # length-only is what the model reads
        horizontal_resolution=list(ds.horizontal_resolution),
        patch_size=[2, 4, 4],
        depths=[1, 1, 1, 1],
        num_heads=[2, 4, 4, 2],
        embed_dim=64,
        window_size=[2, 4, 8],
    ).to(device)

    sampler = LeadTimePairSampler(
        dataset_length=len(ds),
        forecast_lead_times=[1, 4],
        num_samples=2,
        seed=0,
    )
    optimizer = torch.optim.AdamW(model.parameters(), lr=1e-4)
    model.train()
    n_steps = 0
    for start, lead in sampler:
        raw = ds[(start, lead)]
        on_device = {k: v.to(device) for k, v in raw.items()}
        sample = normalizer(on_device)
        # PLASIM lsm carries NaN at high latitudes by convention; without a
        # NaN-fill the patch-embed -> attention path produces NaN losses. A
        # proper NanFillTransform is a follow-up; for the smoke we just zero
        # them out on the boundary fields that can carry them.
        sample["constant_boundary"] = torch.nan_to_num(
            sample["constant_boundary"], nan=0.0
        )
        sample["varying_boundary"] = torch.nan_to_num(
            sample["varying_boundary"], nan=0.0
        )

        optimizer.zero_grad()
        out = model(
            sample["surface_in"].unsqueeze(0),
            sample["constant_boundary"].unsqueeze(0),
            sample["varying_boundary"].unsqueeze(0),
            sample["upper_air_in"].unsqueeze(0),
        )
        loss = sum(t.sum() for t in out if t.requires_grad)
        assert torch.isfinite(loss).all(), (
            f"NaN/inf loss at start={start}, lead={lead}; normalizer or model "
            "regression?"
        )
        loss.backward()
        optimizer.step()
        n_steps += 1
    assert n_steps == 2

    del model, normalizer
    torch.cuda.empty_cache()


@pytest.mark.smoke
@pytest.mark.cuda
@_skip_no_fixture
@_skip_no_stats
def test_plasim_datapipe_with_workers_drives_pangu_plasim():
    """Datapipe wrapper end-to-end on GPU: workers + prefetch + persistent
    workers feeding PanguPlasim forward+backward+step.

    This is the configuration Phase 3's training recipe will use.
    """
    if not torch.cuda.is_available():
        pytest.skip("smoke test requires CUDA")

    ds_for_layout = PlasimClimateDataset(_fixture_path())
    normalizer = PlasimNormalizer.from_dataset(
        ds_for_layout, mean_path=_MEAN_PATH, std_path=_STD_PATH
    )
    pipe = PlasimClimateDatapipe(
        _fixture_path(),
        forecast_lead_times=[1, 4],
        normalizer=normalizer,
        batch_size=2,
        num_samples_per_epoch=4,
        num_workers=2,
        prefetch_factor=2,
        persistent_workers=True,
        pin_memory=True,
        seed=0,
    )

    model = PanguPlasim(
        surface_variables=ds_for_layout.layout.surface_variables,
        upper_air_variables=ds_for_layout.upper_air_variable_names,
        constant_boundary_variables=ds_for_layout.layout.constant_boundary_variables,
        varying_boundary_variables=ds_for_layout.layout.varying_boundary_variables,
        diagnostic_variables=ds_for_layout.layout.diagnostic_variables,
        levels=ds_for_layout.sigma_levels,
        horizontal_resolution=list(ds_for_layout.horizontal_resolution),
        patch_size=[2, 4, 4],
        depths=[1, 1, 1, 1],
        num_heads=[2, 4, 4, 2],
        embed_dim=64,
        window_size=[2, 4, 8],
    ).to("cuda:0")

    optimizer = torch.optim.AdamW(model.parameters(), lr=1e-4)
    model.train()
    n_batches = 0
    for batch in pipe:
        optimizer.zero_grad()
        out = model(
            batch["surface_in"],
            batch["constant_boundary"],
            batch["varying_boundary"],
            batch["upper_air_in"],
        )
        loss = sum(t.sum() for t in out if t.requires_grad)
        assert torch.isfinite(loss).all(), "NaN loss through the datapipe wrapper"
        loss.backward()
        optimizer.step()
        n_batches += 1
    assert n_batches == 2

    del model, pipe
    torch.cuda.empty_cache()
